      ELSIF UPPER(TRIM(v_ticket.priority)) LIKE '%PRIORITÉ 2%' OR UPPER(TRIM(v_ticket.priority)) LIKE '%PRIORITE 2%' OR UPPER(TRIM(v_ticket.priority)) LIKE '%HAUTE%' THEN
