        v_priority := 'High'::priority_t;
      ELSE
        v_priority := 'Low'::priority_t;
      END IF;
      
      -- Mapper le canal
      IF UPPER(TRIM(v_ticket.canal)) LIKE '%APPEL TÉLÉPHONIQUE%' OR UPPER(TRIM(v_ticket.canal)) LIKE '%APPEL TELEPHONIQUE%' THEN
